#include <stdio.h>

/* 5. Ecrivez la procédre **Binaire (int x)** 
 * qui affiche l'équivalent binaire de son praramêtre. */


void bin (int x)
{
    unsigned int b = ~(~0U >> 1);
    int u = 0;

    while (b > 0)
    {
        if (u % 4 == 0 && u != 0) putchar (' ');

        putchar ((b & x) ? '1' : '0');

        b = b >> 1;
        u++;
    }
    printf ("\n");
}

void bin2 (unsigned int x)
{
    if (x / 2 == 0)
    {
        printf ("%u", x % 2);
        return ;
    }
    bin2 (x / 2);
    printf ("%u", x % 2);
    return ;
}

int main (void)
{
    printf ("%s", "procédure Bin :\n");
    for (int x = 1; x < 20; x++)
    {
        printf ("%5d : ", x);
        bin (x);
    }
    putchar ('\n');

    printf ("%s", "procédure Bin2 :\n");
    for (int u = 1; u < 20; u++)
    {
        printf ("%5d : ", u);
        bin2 (u);
        putchar ('\n');
    }
    return 0;
}

