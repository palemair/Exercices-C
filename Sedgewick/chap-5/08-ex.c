#define GNU_SOURCE
#include <stdio.h>
#include <string.h>

#define DIM 36

/* 8. Ecrivez un programme récursif de type diviser pour résoudre pour dessiner une approximation de droite. */

int tb[DIM][DIM];

void afft(void)
{
  putchar('\n');
  printf("    |");
  for(int u = 0; u<DIM; u++) printf("%3d",u);
  putchar('\n');
  printf("%5c",' ');
  for(int u = 0; u<DIM; u++) printf("---");
  putchar('\n');

  for(int u = 0; u<DIM; u++)
  {
      printf("%4d|",u);
      for(int y = 0; y<DIM; y++) 
      {
          printf("%3c",tb[u][y]);
      }
      
      putchar('\n');
  }
  putchar('\n');
}

void point(int x, int y,char c)
{
    if((x>=0 && x<DIM) && (y>=0 && y<DIM))
    {
        tb[y][x] = c;
    }
}

void ligne(int x1,int y1,int x2,int y2)
{
    int x = (x1 + x2) / 2;
    int y = (y1 + y2) / 2;

    if(x>x1 && y<y2)
    {
        point(x,y,'x');
        ligne(x1,y1,x,y);
        ligne(x,y,x2,y2);
    }
}


int main() {
  
  memset(tb,46,sizeof tb);

  int x1,y1,x2,y2;
  
  x1 = 5;
  y1 = 5;
  x2 = 25;
  y2 = 25;
  
  point(x1,y1,'S');
  point(x2,y2,'E');
  printf("\tAffichage des points : x1 =%3d, y1 =%3d, et x2 =%3d, y2 =%3d\n",x1,y1,x2,y2);
  afft();

  ligne(x1,y1,x2,y2);

  printf("\tSegment tracé avec ces points\n");
  afft();

  memset(tb,46,sizeof tb);
  x1 = 5;
  y1 = 15;
  x2 = 20;
  y2 = 25;
  
  point(x1,y1,'S');
  point(x2,y2,'E');
  printf("\tAffichage des points : x1 =%3d, y1 =%3d, et x2 =%3d, y2 =%3d\n",x1,y1,x2,y2);
  afft();

  ligne(x1,y1,x2,y2);

  printf("\tSegment tracé avec ces points\n");
  afft();
  return 0;
}

